        String password = new String(password_original);
        boolean answer=User_Controller.IsUser(TextField_Username.getText(), password); 

        if (answer)
        {
            int rol;
            rol=User_Controller.getUser(TextField_Username.getText()).getRol();
            
            if (rol==1)
            {          
                        adminWindow = new AdminModWindow(TextField_Username.getText());            
                        adminWindow.setVisible(true);
                        this.dispose();                
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Intente de nuevo","Usuario o Contrase√±a Incorrecta",JOptionPane.ERROR_MESSAGE);
        }

